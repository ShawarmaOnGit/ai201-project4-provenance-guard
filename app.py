@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(force=True, silent=True) or {}
    text = data.get("text", "").strip()
    creator_id = data.get("creator_id", "anonymous").strip()

    if not text:
        return jsonify({"error": "text field is required"}), 400
    if len(text) < 20:
        return jsonify({"error": "text must be at least 20 characters"}), 400

    content_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).isoformat()

    llm_score = llm_signal(text)
    stylo_score = stylometric_signal(text)
    scoring = compute_confidence(llm_score, stylo_score)
    label = generate_label(
        scoring["attribution"],
        scoring["confidence"],
        scoring["ai_probability"],
    )

    log_entry = {
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": timestamp,
        "event": "classification",
        "attribution": scoring["attribution"],
        "ai_probability": scoring["ai_probability"],
        "confidence": scoring["confidence"],
        "llm_score": round(llm_score, 4),
        "stylometric_score": round(stylo_score, 4),
        "label": label,
        "status": "classified",
        "appeal": None,
    }
    append_log(log_entry)

    store = load_content_store()
    store[content_id] = {
        "creator_id": creator_id,
        "attribution": scoring["attribution"],
        "status": "classified",
        "timestamp": timestamp,
    }
    save_content_store(store)

    return jsonify({
        "content_id": content_id,
        "attribution": scoring["attribution"],
        "ai_probability": scoring["ai_probability"],
        "confidence": scoring["confidence"],
        "llm_score": round(llm_score, 4),
        "stylometric_score": round(stylo_score, 4),
        "label": label,
        "status": "classified",
        "timestamp": timestamp,
    })


@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(force=True, silent=True) or {}
    content_id = data.get("content_id", "").strip()
    creator_reasoning = data.get("creator_reasoning", "").strip()

    if not content_id:
        return jsonify({"error": "content_id is required"}), 400
    if not creator_reasoning:
        return jsonify({"error": "creator_reasoning is required"}), 400

    store = load_content_store()
    if content_id not in store:
        return jsonify({"error": "content_id not found"}), 404
    if store[content_id]["status"] == "under_review":
        return jsonify({"error": "an appeal for this content is already under review"}), 409

    timestamp = datetime.now(timezone.utc).isoformat()
    store[content_id]["status"] = "under_review"
    save_content_store(store)

    appeal_entry = {
        "content_id": content_id,
        "event": "appeal_submitted",
        "timestamp": timestamp,
        "creator_id": store[content_id]["creator_id"],
        "original_attribution": store[content_id]["attribution"],
        "status": "under_review",
        "appeal_reasoning": creator_reasoning,
    }
    append_log(appeal_entry)

    return jsonify({
        "content_id": content_id,
        "status": "under_review",
        "message": (
            "Your appeal has been received and logged. "
            "A human reviewer will assess your submission."
        ),
        "timestamp": timestamp,
    })


@app.route("/log", methods=["GET"])
def audit_log():
    limit = request.args.get("limit", 50, type=int)
    entries = get_log(limit=limit)
    return jsonify({"entries": entries, "count": len(entries)})


@app.errorhandler(429)
def rate_limit_exceeded(e):
    return jsonify({
        "error": "Rate limit exceeded",
        "message": "Too many submissions. Limit: 10 per minute, 100 per day. Please wait before submitting again.",
        "retry_after": str(e.description),
    }), 429


if __name__ == "__main__":
    app.run(debug=True, port=5001)
